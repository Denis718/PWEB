
var op1 = Number(window.prompt('Número 1: '))
var op2 = Number(window.prompt('Numero 2: '))

var soma = op1 + op2
var sub = op1 - op2
var prod = op1 * op2
var div = op1 / op2
var resto = op1 % op2

confirm(`Soma = ${soma}`)
confirm(`Subtração = ${sub}`)
confirm(`Produto = ${prod}`)
confirm(`Divisão = ${div}`)
confirm(`Resto = ${resto}`)
